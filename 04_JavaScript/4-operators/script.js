// Operators
// 1. Assignment Operator
let num1 = 2;
let num2 = 5;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 % num2);

//2. Urinary Operators
let marks = 100;
console.log(marks++); //post increment
console.log(marks--); //post increment

console.log(++marks); //pre increment
console.log(--marks); //pre decrement
